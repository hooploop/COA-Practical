.MODEL SMALL
.STACK 64

.DATA
INPUT1 DB ?
RESULT DB ?
PROMPT DB "Please enter a digit: $"

.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	
	;PROMPT
	MOV AH, 09H
	LEA DX, PROMPT 
	INT 21H
	
	;INPUT
	MOV AH, 01H
	INT 21H
	MOV INPUT1, AL
	
	;ASCII ADJUSTMENT
	SUB INPUT1, 30H
	
	;ENTER A NEW LINE
	MOV AH, 02H
	MOV DL, 0AH 
	INT 21H
	MOV DL, 0DH
	INT 21H
	
	;CALCULATE
	XOR AX,AX
	MOV AL, INPUT1
	MUL AL
	
	MOV RESULT, AL
	
	;DISPLAY RESULT
	MOV AH, 02H
	MOV DL, RESULT
	
	;ASCII ADJUSTMENT
	ADD DL, 30H
	INT 21H
	
	;TERMINATE
	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN