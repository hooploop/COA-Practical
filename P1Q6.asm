.MODEL SMALL
.STACK 64

.DATA
INPUT1 DB ?
RESULT1 DB ?
RESULT2 DB ?
PROMPT DB "Enter a digit: $"
VAR1 DB ?
VAR2 DB ?

.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	
	;PROMPT
	MOV AH, 09H
	LEA DX, PROMPT      
	INT 21H
	
	;INPUT
	MOV AH, 01H
	INT 21H
	MOV INPUT1, AL
	
	;ASCII ADJUSTMENT
	SUB INPUT1, 30H
	
	;ENTER A NEW LINE
	MOV AH, 02H
	MOV DL, 0AH        
	INT 21H
	MOV DL, 0DH			
	INT 21H
	
	;CALCULATE
	XOR AX,AX		
	MOV AL, INPUT1
	MUL AL
	
	MOV BL, 10;
	DIV BL
	MOV RESULT1, AL;
	MOV RESULT2, AH;
	ADD RESULT1, 30H
	ADD RESULT2, 30H
	;DISPLAY RESULT
	MOV AH, 02H
	MOV DL, RESULT1
	INT 21H
	MOV DL, RESULT2
	INT 21H
	
	;--terminate
	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN